// we will fetch data via API or simulate it for the UI presentation.
// But wait, the view DOES send total_students, present_today, absent_today, attendance_percentage!

document.addEventListener("DOMContentLoaded", function () {
    const presentCountStr = document.getElementById('presentTodayStat') ? document.getElementById('presentTodayStat').innerText : '0';
    const absentCountStr = document.getElementById('absentTodayStat') ? document.getElementById('absentTodayStat').innerText : '0';

    const presentCount = parseInt(presentCountStr) || 0;
    const absentCount = parseInt(absentCountStr) || 0;

    // Pie Chart Example
    var ctx = document.getElementById("myPieChart");
    if (ctx) {
        var myPieChart = new Chart(ctx, {
            type: 'doughnut',
            data: {
                labels: ["Present", "Absent"],
                datasets: [{
                    data: [presentCount, absentCount],
                    backgroundColor: ['#1cc88a', '#e74a3b'],
                    hoverBackgroundColor: ['#17a673', '#e02d1b'],
                    hoverBorderColor: "rgba(234, 236, 244, 1)",
                }],
            },
            options: {
                maintainAspectRatio: false,
                tooltips: {
                    backgroundColor: "rgb(255,255,255)",
                    bodyFontColor: "#858796",
                    borderColor: '#dddfeb',
                    borderWidth: 1,
                    xPadding: 15,
                    yPadding: 15,
                    displayColors: false,
                    caretPadding: 10,
                },
                legend: {
                    display: false
                },
                cutoutPercentage: 80,
            },
        });
    }
});
